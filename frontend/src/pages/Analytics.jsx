import { useEffect, useMemo, useState } from "react";
import jsPDF from "jspdf";
import autoTable from "jspdf-autotable";
import * as XLSX from "xlsx";
import api from "../api/axiosConfig";

const categories = [
  { id: "users", label: "Users" },
  { id: "bookings", label: "Bookings" },
  { id: "maintenance", label: "Maintenance" },
  { id: "assets", label: "Assets" },
  { id: "notifications", label: "Notifications" },
];

function formatDate(value) {
  if (!value) {
    return "-";
  }
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return "-";
  }
  return date.toLocaleString();
}

function getPercent(part, whole) {
  if (!whole) {
    return 0;
  }
  return Number(((part / whole) * 100).toFixed(1));
}

function createPdfHeader(doc, title, subtitle) {
  doc.setFillColor(15, 23, 42);
  doc.rect(0, 0, 210, 34, "F");

  doc.setTextColor(34, 211, 238);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(10);
  doc.text("UNIOPS ADMIN REPORT", 14, 10);

  doc.setTextColor(255, 255, 255);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(18);
  doc.text(title, 14, 20);

  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  doc.setTextColor(203, 213, 225);
  doc.text(subtitle, 14, 27);

  doc.setDrawColor(8, 47, 73);
  doc.setLineWidth(0.6);
  doc.line(14, 36, 196, 36);
}

function createPdfFooter(doc) {
  const pageCount = doc.internal.getNumberOfPages();
  for (let i = 1; i <= pageCount; i += 1) {
    doc.setPage(i);
    doc.setDrawColor(226, 232, 240);
    doc.setLineWidth(0.2);
    doc.line(14, 286, 196, 286);
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    doc.setTextColor(71, 85, 105);
    doc.text(
      `Generated by UniOps Analytics on ${new Date().toLocaleString()} | Page ${i}/${pageCount}`,
      14,
      292,
    );
  }
}

function Analytics({ user, onBack }) {
  const [activeCategory, setActiveCategory] = useState("users");
  const [dashboard, setDashboard] = useState(null);
  const [notifications, setNotifications] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState("");

  const totalUsers = dashboard?.totalUsers ?? 0;
  const blockedUsers = dashboard?.blockedUsers ?? 0;
  const activeUsers = Math.max(0, totalUsers - blockedUsers);
  const totalLogins = dashboard?.totalLogins ?? 0;
  const users = dashboard?.users || [];
  const loginChart = dashboard?.loginChart || [];

  const activePercent = getPercent(activeUsers, totalUsers);
  const blockedPercent = getPercent(blockedUsers, totalUsers);

  const loginBars = useMemo(() => {
    const ranked = [...loginChart].sort(
      (a, b) => (b.loginCount || 0) - (a.loginCount || 0),
    );
    const top = ranked.slice(0, 8);
    const peak = Math.max(1, ...top.map((item) => item.loginCount || 0));

    return top.map((item) => ({
      ...item,
      barPercent: ((item.loginCount || 0) / peak) * 100,
    }));
  }, [loginChart]);

  const notificationsWithAttachment = useMemo(
    () => notifications.filter((item) => item.hasDocument).length,
    [notifications],
  );

  const notificationsWithLinks = useMemo(
    () => notifications.filter((item) => item.linkUrl).length,
    [notifications],
  );

  useEffect(() => {
    const load = async () => {
      setLoading(true);
      setError("");

      try {
        const [dashboardResponse, notificationsResponse] = await Promise.all([
          api.get("/admin/dashboard"),
          api.get("/notifications"),
        ]);

        if (dashboardResponse.data?.success) {
          setDashboard(dashboardResponse.data.data);
        } else {
          setError(
            dashboardResponse.data?.message || "Unable to load analytics data.",
          );
        }

        if (notificationsResponse.data?.success) {
          setNotifications(notificationsResponse.data.data || []);
        }
      } catch (err) {
        setError(
          err.response?.data?.message || "Unable to load analytics data.",
        );
      } finally {
        setLoading(false);
      }
    };

    load();
  }, []);

  const exportUsersPdf = () => {
    const doc = new jsPDF("p", "mm", "a4");
    createPdfHeader(
      doc,
      "Users Analytics Report",
      `${user?.name || "Admin"} | ${new Date().toLocaleDateString()}`,
    );

    doc.setFont("helvetica", "bold");
    doc.setTextColor(15, 23, 42);
    doc.setFontSize(11);
    doc.text("Executive Summary", 14, 46);

    autoTable(doc, {
      startY: 50,
      theme: "grid",
      styles: { fontSize: 10, cellPadding: 3 },
      headStyles: { fillColor: [30, 41, 59], textColor: [255, 255, 255] },
      body: [
        ["Total Users", totalUsers],
        ["Active Users", `${activeUsers} (${activePercent}%)`],
        ["Blocked Users", `${blockedUsers} (${blockedPercent}%)`],
        ["Total Login Events", totalLogins],
      ],
      columnStyles: {
        0: {
          fontStyle: "bold",
          fillColor: [241, 245, 249],
          textColor: [15, 23, 42],
        },
      },
      margin: { left: 14, right: 14 },
    });

    const tableRows = users.map((item) => [
      item.name || "-",
      item.email || "-",
      item.role || "-",
      item.blocked ? "Blocked" : "Active",
      item.loginCount ?? 0,
      formatDate(item.lastLoginAt),
      formatDate(item.createdAt),
    ]);

    autoTable(doc, {
      startY: doc.lastAutoTable.finalY + 10,
      head: [
        ["Name", "Email", "Role", "Status", "Logins", "Last Login", "Created"],
      ],
      body: tableRows,
      styles: { fontSize: 8.6, cellPadding: 2.4 },
      headStyles: { fillColor: [8, 47, 73], textColor: [255, 255, 255] },
      alternateRowStyles: { fillColor: [248, 250, 252] },
      margin: { left: 14, right: 14 },
      didDrawPage: () => {
        createPdfFooter(doc);
      },
    });

    createPdfFooter(doc);
    doc.save(
      `uniops-users-report-${new Date().toISOString().slice(0, 10)}.pdf`,
    );
  };

  const exportUsersExcel = () => {
    const summaryRows = [
      { Metric: "Total Users", Value: totalUsers },
      {
        Metric: "Active Users",
        Value: activeUsers,
        Percentage: `${activePercent}%`,
      },
      {
        Metric: "Blocked Users",
        Value: blockedUsers,
        Percentage: `${blockedPercent}%`,
      },
      { Metric: "Total Login Events", Value: totalLogins },
    ];

    const userRows = users.map((item) => ({
      Name: item.name || "-",
      Email: item.email || "-",
      Role: item.role || "-",
      Status: item.blocked ? "Blocked" : "Active",
      "Login Count": item.loginCount ?? 0,
      "Last Login": formatDate(item.lastLoginAt),
      "Created At": formatDate(item.createdAt),
    }));

    const workbook = XLSX.utils.book_new();
    XLSX.utils.book_append_sheet(
      workbook,
      XLSX.utils.json_to_sheet(summaryRows),
      "Summary",
    );
    XLSX.utils.book_append_sheet(
      workbook,
      XLSX.utils.json_to_sheet(userRows),
      "Users",
    );

    XLSX.writeFile(
      workbook,
      `uniops-users-report-${new Date().toISOString().slice(0, 10)}.xlsx`,
    );
  };

  const exportNotificationsPdf = () => {
    const doc = new jsPDF("p", "mm", "a4");
    createPdfHeader(
      doc,
      "Notifications Report",
      `${user?.name || "Admin"} | ${new Date().toLocaleDateString()}`,
    );

    autoTable(doc, {
      startY: 44,
      theme: "grid",
      styles: { fontSize: 10, cellPadding: 3 },
      headStyles: { fillColor: [30, 41, 59], textColor: [255, 255, 255] },
      body: [
        ["Total Notifications", notifications.length],
        ["With Attachments", notificationsWithAttachment],
        ["With Links", notificationsWithLinks],
      ],
      margin: { left: 14, right: 14 },
      columnStyles: {
        0: {
          fontStyle: "bold",
          fillColor: [241, 245, 249],
          textColor: [15, 23, 42],
        },
      },
    });

    const rows = notifications.map((item) => [
      item.title || "-",
      item.createdBy || "-",
      item.linkUrl ? "Yes" : "No",
      item.hasDocument ? "Yes" : "No",
      formatDate(item.createdAt),
    ]);

    autoTable(doc, {
      startY: doc.lastAutoTable.finalY + 10,
      head: [["Title", "Created By", "Has Link", "Has Document", "Created"]],
      body: rows,
      styles: { fontSize: 9, cellPadding: 2.4 },
      headStyles: { fillColor: [8, 47, 73], textColor: [255, 255, 255] },
      alternateRowStyles: { fillColor: [248, 250, 252] },
      margin: { left: 14, right: 14 },
      didDrawPage: () => {
        createPdfFooter(doc);
      },
    });

    createPdfFooter(doc);
    doc.save(
      `uniops-notifications-report-${new Date().toISOString().slice(0, 10)}.pdf`,
    );
  };

  const exportNotificationsExcel = () => {
    const summaryRows = [
      { Metric: "Total Notifications", Value: notifications.length },
      { Metric: "With Attachments", Value: notificationsWithAttachment },
      { Metric: "With Links", Value: notificationsWithLinks },
    ];

    const details = notifications.map((item) => ({
      Title: item.title || "-",
      Message: item.message || "-",
      "Created By": item.createdBy || "-",
      "Has Link": item.linkUrl ? "Yes" : "No",
      "Link URL": item.linkUrl || "-",
      "Has Attachment": item.hasDocument ? "Yes" : "No",
      "Attachment Name": item.documentName || "-",
      "Document URL": item.documentUrl || "-",
      "Created At": formatDate(item.createdAt),
    }));

    const workbook = XLSX.utils.book_new();
    XLSX.utils.book_append_sheet(
      workbook,
      XLSX.utils.json_to_sheet(summaryRows),
      "Summary",
    );
    XLSX.utils.book_append_sheet(
      workbook,
      XLSX.utils.json_to_sheet(details),
      "Notifications",
    );

    XLSX.writeFile(
      workbook,
      `uniops-notifications-report-${new Date().toISOString().slice(0, 10)}.xlsx`,
    );
  };

  const pieCircleLength = 2 * Math.PI * 54;
  const blockedArcLength = (blockedPercent / 100) * pieCircleLength;

  return (
    <main className="relative overflow-hidden">
      <div className="pointer-events-none absolute inset-0 bg-[radial-gradient(circle_at_20%_20%,_rgba(56,189,248,0.16),_transparent_35%),radial-gradient(circle_at_80%_0%,_rgba(16,185,129,0.14),_transparent_35%),radial-gradient(circle_at_70%_80%,_rgba(251,191,36,0.1),_transparent_38%)]" />

      <section className="relative mx-auto max-w-7xl px-4 py-8 sm:px-6 lg:px-8">
        <div className="flex flex-wrap items-center justify-between gap-3">
          <div>
            <p className="text-xs font-semibold uppercase tracking-[0.3em] text-cyan-300">
              Admin Intelligence
            </p>
            <h1 className="mt-2 text-4xl font-black text-white">
              Analytics Center
            </h1>
            <p className="mt-2 text-sm text-slate-300">
              Build reports and monitor platform activity with executive-level
              summaries.
            </p>
          </div>
          <button
            type="button"
            onClick={onBack}
            className="rounded-xl border border-slate-700 bg-slate-900/70 px-4 py-2 text-sm font-semibold text-slate-200 transition hover:border-cyan-400/50 hover:bg-slate-800"
          >
            Back to Dashboard
          </button>
        </div>

        <div className="mt-6 flex flex-wrap gap-2">
          {categories.map((category) => (
            <button
              key={category.id}
              type="button"
              onClick={() => setActiveCategory(category.id)}
              className={`rounded-xl px-4 py-2 text-sm font-semibold transition ${
                activeCategory === category.id
                  ? "bg-cyan-500 text-slate-950"
                  : "border border-slate-700 bg-slate-900/70 text-slate-200 hover:border-cyan-500/60 hover:bg-slate-800"
              }`}
            >
              {category.label}
            </button>
          ))}
        </div>

        {error && (
          <div className="mt-6 rounded-2xl border border-rose-500/30 bg-rose-500/10 px-4 py-3 text-sm text-rose-200">
            {error}
          </div>
        )}

        {loading ? (
          <div className="mt-6 rounded-3xl border border-slate-800 bg-slate-900/80 p-6 text-slate-300">
            Loading analytics data...
          </div>
        ) : (
          <>
            {activeCategory === "users" && (
              <section className="mt-6 grid gap-6">
                <div className="grid gap-4 sm:grid-cols-2 lg:grid-cols-4">
                  <MetricCard
                    label="Total Users"
                    value={totalUsers}
                    accent="cyan"
                  />
                  <MetricCard
                    label="Active Users"
                    value={`${activeUsers} (${activePercent}%)`}
                    accent="emerald"
                  />
                  <MetricCard
                    label="Blocked Users"
                    value={`${blockedUsers} (${blockedPercent}%)`}
                    accent="rose"
                  />
                  <MetricCard
                    label="Total Logins"
                    value={totalLogins}
                    accent="amber"
                  />
                </div>

                <div className="grid gap-6 lg:grid-cols-[1.6fr,1fr]">
                  <article className="rounded-3xl border border-slate-800 bg-slate-900/80 p-5 shadow-xl shadow-cyan-950/10">
                    <div className="flex items-center justify-between gap-3">
                      <h2 className="text-xl font-bold text-white">
                        User Login Activity
                      </h2>
                      <span className="text-xs uppercase tracking-[0.2em] text-slate-400">
                        Top active users
                      </span>
                    </div>
                    <div className="mt-5 space-y-4">
                      {loginBars.length === 0 ? (
                        <p className="text-sm text-slate-300">
                          No login activity data available.
                        </p>
                      ) : (
                        loginBars.map((item) => (
                          <div key={item.id}>
                            <div className="mb-1 flex items-center justify-between text-sm">
                              <span className="truncate pr-3 font-semibold text-slate-200">
                                {item.name}
                              </span>
                              <span className="text-cyan-300">
                                {item.loginCount} logins
                              </span>
                            </div>
                            <div className="h-2.5 rounded-full bg-slate-800">
                              <div
                                className="h-2.5 rounded-full bg-gradient-to-r from-cyan-400 via-blue-500 to-indigo-500"
                                style={{
                                  width: `${Math.max(item.barPercent, 4)}%`,
                                }}
                              />
                            </div>
                          </div>
                        ))
                      )}
                    </div>
                  </article>

                  <article className="rounded-3xl border border-slate-800 bg-slate-900/80 p-5 shadow-xl shadow-cyan-950/10">
                    <h2 className="text-xl font-bold text-white">
                      Active vs Blocked
                    </h2>
                    <p className="mt-1 text-sm text-slate-300">
                      User distribution by account status.
                    </p>
                    <div className="mt-5 flex flex-col items-center">
                      <svg
                        width="170"
                        height="170"
                        viewBox="0 0 170 170"
                        className="drop-shadow"
                      >
                        <circle
                          cx="85"
                          cy="85"
                          r="54"
                          fill="none"
                          stroke="#1e293b"
                          strokeWidth="20"
                        />
                        <circle
                          cx="85"
                          cy="85"
                          r="54"
                          fill="none"
                          stroke="#f43f5e"
                          strokeWidth="20"
                          strokeDasharray={`${blockedArcLength} ${pieCircleLength - blockedArcLength}`}
                          strokeLinecap="round"
                          transform="rotate(-90 85 85)"
                        />
                        <circle
                          cx="85"
                          cy="85"
                          r="54"
                          fill="none"
                          stroke="#10b981"
                          strokeWidth="20"
                          strokeDasharray={`${pieCircleLength - blockedArcLength} ${blockedArcLength}`}
                          strokeDashoffset={-blockedArcLength}
                          strokeLinecap="round"
                          transform="rotate(-90 85 85)"
                        />
                        <text
                          x="85"
                          y="84"
                          textAnchor="middle"
                          fill="#ffffff"
                          fontSize="20"
                          fontWeight="700"
                        >
                          {totalUsers}
                        </text>
                        <text
                          x="85"
                          y="102"
                          textAnchor="middle"
                          fill="#94a3b8"
                          fontSize="10"
                        >
                          Users
                        </text>
                      </svg>
                      <div className="mt-5 w-full space-y-2 text-sm">
                        <LegendRow
                          label="Active"
                          value={`${activeUsers} (${activePercent}%)`}
                          tone="emerald"
                        />
                        <LegendRow
                          label="Blocked"
                          value={`${blockedUsers} (${blockedPercent}%)`}
                          tone="rose"
                        />
                      </div>
                    </div>
                  </article>
                </div>

                <article className="rounded-3xl border border-slate-800 bg-slate-900/80 p-5 shadow-xl shadow-cyan-950/10">
                  <div className="flex flex-wrap items-center justify-between gap-3">
                    <h2 className="text-xl font-bold text-white">
                      Users Report Generation
                    </h2>
                    <div className="flex gap-2">
                      <button
                        type="button"
                        onClick={exportUsersPdf}
                        className="rounded-lg bg-cyan-500 px-4 py-2 text-sm font-semibold text-slate-950 transition hover:bg-cyan-400"
                      >
                        Export PDF
                      </button>
                      <button
                        type="button"
                        onClick={exportUsersExcel}
                        className="rounded-lg border border-cyan-400/50 bg-cyan-500/10 px-4 py-2 text-sm font-semibold text-cyan-200 transition hover:bg-cyan-500/20"
                      >
                        Export Excel
                      </button>
                    </div>
                  </div>
                  <p className="mt-2 text-sm text-slate-300">
                    Includes user counts, status percentages, login metrics, and
                    full user detail rows.
                  </p>
                </article>
              </section>
            )}

            {(activeCategory === "bookings" ||
              activeCategory === "maintenance" ||
              activeCategory === "assets") && (
              <section className="mt-6 rounded-3xl border border-amber-400/30 bg-amber-500/10 p-6 text-amber-100">
                <h2 className="text-2xl font-black">
                  {activeCategory.toUpperCase()}
                </h2>
                <p className="mt-2 text-sm">
                  Not implemented yet. Reporting for this category will be added
                  in a future update.
                </p>
              </section>
            )}

            {activeCategory === "notifications" && (
              <section className="mt-6 grid gap-6">
                <div className="grid gap-4 sm:grid-cols-3">
                  <MetricCard
                    label="Total Notifications"
                    value={notifications.length}
                    accent="cyan"
                  />
                  <MetricCard
                    label="With Attachments"
                    value={notificationsWithAttachment}
                    accent="emerald"
                  />
                  <MetricCard
                    label="With Links"
                    value={notificationsWithLinks}
                    accent="amber"
                  />
                </div>

                <article className="rounded-3xl border border-slate-800 bg-slate-900/80 p-5 shadow-xl shadow-cyan-950/10">
                  <div className="flex flex-wrap items-center justify-between gap-3">
                    <h2 className="text-xl font-bold text-white">
                      Notifications Report Generation
                    </h2>
                    <div className="flex gap-2">
                      <button
                        type="button"
                        onClick={exportNotificationsPdf}
                        className="rounded-lg bg-cyan-500 px-4 py-2 text-sm font-semibold text-slate-950 transition hover:bg-cyan-400"
                      >
                        Export PDF
                      </button>
                      <button
                        type="button"
                        onClick={exportNotificationsExcel}
                        className="rounded-lg border border-cyan-400/50 bg-cyan-500/10 px-4 py-2 text-sm font-semibold text-cyan-200 transition hover:bg-cyan-500/20"
                      >
                        Export Excel
                      </button>
                    </div>
                  </div>
                  <p className="mt-2 text-sm text-slate-300">
                    Includes notification metadata, attachment/link presence,
                    and creation timeline.
                  </p>

                  <div className="mt-5 overflow-x-auto">
                    <table className="min-w-full border-separate border-spacing-y-2">
                      <thead>
                        <tr className="text-left text-xs uppercase tracking-[0.2em] text-slate-400">
                          <th className="px-3 py-2">Title</th>
                          <th className="px-3 py-2">Creator</th>
                          <th className="px-3 py-2">Link</th>
                          <th className="px-3 py-2">Document</th>
                          <th className="px-3 py-2">Created At</th>
                        </tr>
                      </thead>
                      <tbody>
                        {notifications.length === 0 ? (
                          <tr>
                            <td
                              colSpan="5"
                              className="px-3 py-4 text-center text-slate-300"
                            >
                              No notifications available.
                            </td>
                          </tr>
                        ) : (
                          notifications.slice(0, 10).map((item) => (
                            <tr
                              key={item.id}
                              className="rounded-2xl bg-slate-950/60"
                            >
                              <td className="rounded-l-2xl px-3 py-3 text-sm font-semibold text-white">
                                {item.title}
                              </td>
                              <td className="px-3 py-3 text-sm text-slate-300">
                                {item.createdBy || "-"}
                              </td>
                              <td className="px-3 py-3 text-sm text-slate-300">
                                {item.linkUrl ? "Yes" : "No"}
                              </td>
                              <td className="px-3 py-3 text-sm text-slate-300">
                                {item.hasDocument
                                  ? item.documentName || "Yes"
                                  : "No"}
                              </td>
                              <td className="rounded-r-2xl px-3 py-3 text-sm text-slate-300">
                                {formatDate(item.createdAt)}
                              </td>
                            </tr>
                          ))
                        )}
                      </tbody>
                    </table>
                  </div>
                </article>
              </section>
            )}
          </>
        )}
      </section>
    </main>
  );
}

function MetricCard({ label, value, accent }) {
  const accents = {
    cyan: "border-cyan-500/30 bg-cyan-500/10 text-cyan-200",
    emerald: "border-emerald-500/30 bg-emerald-500/10 text-emerald-200",
    rose: "border-rose-500/30 bg-rose-500/10 text-rose-200",
    amber: "border-amber-500/30 bg-amber-500/10 text-amber-200",
  };

  return (
    <article className={`rounded-2xl border p-4 ${accents[accent]}`}>
      <p className="text-xs font-semibold uppercase tracking-[0.2em]">
        {label}
      </p>
      <p className="mt-2 text-3xl font-black text-white">{value}</p>
    </article>
  );
}

function LegendRow({ label, value, tone }) {
  const tones = {
    emerald: "bg-emerald-500",
    rose: "bg-rose-500",
  };

  return (
    <div className="flex items-center justify-between rounded-xl border border-slate-800 bg-slate-950/70 px-3 py-2">
      <div className="flex items-center gap-2">
        <span className={`h-2.5 w-2.5 rounded-full ${tones[tone]}`} />
        <span className="text-slate-200">{label}</span>
      </div>
      <span className="font-semibold text-white">{value}</span>
    </div>
  );
}

export default Analytics;
